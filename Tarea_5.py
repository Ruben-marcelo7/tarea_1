def concatenar_palabras():
  """
  Concatena dos palabras ingresadas por el usuario y muestra el resultado.
  """
  palabra1 = input("Ingrese la primera palabra: ")

  palabra2 = input("Ingrese la segunda palabra: ")

  palabra_concatenada = palabra1 + " " + palabra2

  print(f"Las palabras concatenadas son: {palabra_concatenada}")

concatenar_palabras()
